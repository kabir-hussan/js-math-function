/**
 * Math.random() function is used for get random number
 */


const random1= Math.random(); // It will give a random number with fraction btn 0 to 1;

const random2= Math.random()*10; // It will give a random number with fraction btn 1 to 10;

const random3= Math.round(Math.random()*10); //It provide a random number without fraction btn 1 to 10

console.log('Random Number :', random1);

console.log('Random Number :', random2);

console.log('Random Number :', random3);

